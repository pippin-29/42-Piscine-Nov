char	*ft_strlowcase(char *str)
{
	int		i;
	char	set;

	i = 0;
	while (*(str + i) != 0)
	{
		set = *(str + i);
		if (set >= 65 && set <= 90)
		{
			*(str + i) += 32;
		}
		else
		{
			*(str + i) = *(str + i);
		}
		i++;
	}
	return (str);
}
