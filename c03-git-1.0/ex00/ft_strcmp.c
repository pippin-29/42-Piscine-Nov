int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	diff;

	diff = 0;
	i = 0;
	while ((*(s1 + i) == *(s2 + i)) && (*(s1 + i) != 0) && (*(s2 + i) != 0))
	{
		i++;
	}
	if ((s1[i] - s2[i]) > 0)
	{
		diff = 1;
	}
	else if ((s1[i] - s2[i]) < 0)
	{
		diff = -1;
	}
	else
	{
		diff = 0;
	}
	return (diff);
}
